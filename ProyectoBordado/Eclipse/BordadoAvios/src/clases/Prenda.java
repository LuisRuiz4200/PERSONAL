package clases;

public class Prenda {

	private int codPrenda;
	private String desPrenda;
	
	public Prenda(int codPrenda, String desPrenda) {
		super();
		this.codPrenda = codPrenda;
		this.desPrenda = desPrenda;
	}
	
	public int getCodPrenda() {
		return codPrenda;
	}
	public void setCodPrenda(int codPrenda) {
		this.codPrenda = codPrenda;
	}
	public String getDesPrenda() {
		return desPrenda;
	}
	public void setDesPrenda(String desPrenda) {
		this.desPrenda = desPrenda;
	}
	
	
}
